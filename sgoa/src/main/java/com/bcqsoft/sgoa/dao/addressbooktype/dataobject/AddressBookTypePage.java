package com.bcqsoft.sgoa.dao.addressbooktype.dataobject;

import java.util.List;

import com.bcqsoft.sgoa.core.base.BasePage;

public class AddressBookTypePage extends BasePage{
	
	/**
	 * 序列化ID
	 */
	private static final long serialVersionUID = -5622021560288626237L;
	
	/**
	 * 通讯录集合
	 */
	List<AddressBookType> addressTypeList;
	
	/**
	 * 排序号
	 */
	private Integer sorting;
	
	/**
	 * 分组名称
	 */
	private String typeName;
	
	/**
	 * 用户ID(关联SCOA_TB_USER.LOGIN_ID)
	 */
	private String loginId;
	
	/**
	 * 生成Getter和Setter方法
	 */

	public List<AddressBookType> getAddressTypeList() {
		return addressTypeList;
	}

	public void setAddressTypeList(List<AddressBookType> addressTypeList) {
		this.addressTypeList = addressTypeList;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getLoginId() {
		return loginId;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

	public Integer getSorting() {
		return sorting;
	}

	public void setSorting(Integer sorting) {
		this.sorting = sorting;
	}
	
}