package com.bcqsoft.sgoa.mvc.form.optlog;

import java.util.Date;

import com.bcqsoft.sgoa.core.base.BaseForm;

/**
 * 操作日志表ORM对象
 * 
 * <pre>
 * 表: SCOA_TB_OPT_LOG
 * </pre>
 */
public class OptLogForm extends BaseForm {


	/**
	 * 自增型主键
	 */
	private Long id;

	/**
	 * 创建日期
	 */
	private Date createDate;

	/**
	 * 更新日期
	 */
	private Date updateDate;

	/**
	 * 操作人ID(关联SCOA_TB_USER.LOGIN_ID)
	 */
	private String optId;
	
	/**
	 * 操作人名称(关联SCOA_TB_USER.USER_NAME)
	 */
	private String optName;

	/**
	 * IP地址
	 */
	private String optIp;

	/**
	 * MAC地址
	 */
	private String optMac;
	
	/**
	 * 开始时间
	 */
	private String startTime;
	
	/**
	 * 结束时间
	 */
	private String endTime;

	/**
	 * 取得自增型主键
	 */
	public Long getId() {
		return id;
	}

	/**
	 * 设置自增型主键
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * 取得创建日期
	 */
	public Date getCreateDate() {
		return createDate;
	}

	/**
	 * 设置创建日期
	 */
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	/**
	 * 取得更新日期
	 */
	public Date getUpdateDate() {
		return updateDate;
	}

	/**
	 * 设置更新日期
	 */
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	/**
	 * 取得操作人ID(关联SCOA_TB_USER.LOGIN_ID)
	 */
	public String getOptId() {
		return optId;
	}

	/**
	 * 设置操作人ID(关联SCOA_TB_USER.LOGIN_ID)
	 */
	public void setOptId(String optId) {
		this.optId = optId;
	}
	
	/**
	 * 取得操作人名称
	 */
	public String getOptName() {
		return optName;
	}

	/**
	 * 设置操作人名称
	 */
	public void setOptName(String optName) {
		this.optName = optName;
	}

	
	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getOptIp() {
		return optIp;
	}

	public void setOptIp(String optIp) {
		this.optIp = optIp;
	}

	public String getOptMac() {
		return optMac;
	}

	public void setOptMac(String optMac) {
		this.optMac = optMac;
	}
	
}
