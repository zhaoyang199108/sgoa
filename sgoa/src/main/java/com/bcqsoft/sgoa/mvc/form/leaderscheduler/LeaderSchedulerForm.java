package com.bcqsoft.sgoa.mvc.form.leaderscheduler;

import java.util.Date;
import com.bcqsoft.sgoa.core.base.BaseForm;


/**
 * 日程表(scao_tb_schedule)数据ORM对象
 * 
 * @author Bcqsoft.com cql
 * 
 */
public class LeaderSchedulerForm extends BaseForm {

	// Enabled字段值(N:不可用, Y:可用)
	public static final String DISABLED = "N";
	public static final String ABLED = "Y";

	/**
	 * 自增型主键
	 */
	private Long id;

	/**
	 * 创建日期
	 */
	private Date createDate;

	/**
	 * 更新日期
	 */
	private Date updateDate;

	/**
	 * 用户ID(关联SOCA_TB_USER.LOGIN_ID)
	 */
	private String loginId;
	
	/**
	 * 用户ID(关联SOCA_TB_USER.LOGIN_ID)
	 */
	private String userName;

	/**
	 * 日程开始时间(格式: 2011-12-16 10:30:55)
	 */
	private String startTime;

	/**
	 * 日程结束时间(格式: 2011-12-16 10:30:55)
	 */
	private String endTime;

	/**
	 * 日程内容
	 */
	private String content;

	/**
	 * 开始提醒时间(格式: 2011-12-16 10:30:55)
	 */
	private String startRemindTime;

	/**
	 * 是否继续提醒(Y:提醒, N:不提醒)
	 */
	private String isRemind;

	/**
	 * Y: 有效, N无效
	 */
	private String enabled;

	/**
	 * ===================<br>
	 * Getter&Setter<br>
	 * ===================
	 */

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	public String getLoginId() {
		return loginId;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getStartRemindTime() {
		return startRemindTime;
	}

	public void setStartRemindTime(String startRemindTime) {
		this.startRemindTime = startRemindTime;
	}

	public String getIsRemind() {
		return isRemind;
	}

	public void setIsRemind(String isRemind) {
		this.isRemind = isRemind;
	}

	public String getEnabled() {
		return enabled;
	}

	public void setEnabled(String enabled) {
		this.enabled = enabled;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
}
