package com.bcqsoft.sgoa.mvc.form.resfolder;

import java.util.Date;

import com.bcqsoft.sgoa.core.base.BaseForm;

/**
 * 部门科室表(记录当前单位科室及下属单位科室)ORM对象
 * 
 * <pre>
 * 表: SCOA_TB_DEPT
 * </pre>
 */
public class ResFolderForm extends BaseForm {

	/**
	 * 自增型主键
	 */
	private Long id;

	/**
	 * 创建日期
	 */
	private Date createDate;

	/**
	 * 更新日期
	 */
	private Date updateDate;

	/**
	 * 部门名称
	 */
	private String folderName;

	/**
	 * 所属单位ID(关联SCOA_TB_UNIT.ID)
	 */
	private Long unitId;

	/**
	 * 是否是有效记录(Y:有效, N:无效)
	 */
	private String enabled;

	/**
	 * 取得自增型主键
	 */
	public Long getId() {
		return id;
	}

	/**
	 * 设置自增型主键
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * 取得创建日期
	 */
	public Date getCreateDate() {
		return createDate;
	}

	/**
	 * 设置创建日期
	 */
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	/**
	 * 取得更新日期
	 */
	public Date getUpdateDate() {
		return updateDate;
	}

	/**
	 * 设置更新日期
	 */
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	/**
	 * 取得文件夹名称
	 */
	public String getFolderName() {
		return folderName;
	}

	/**
	 * 设置文件夹名称
	 */
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}

	/**
	 * 取得所属单位ID(关联SCOA_TB_UNIT.ID)
	 */
	public Long getUnitId() {
		return unitId;
	}

	/**
	 * 设置所属单位ID(关联SCOA_TB_UNIT.ID)
	 */
	public void setUnitId(Long unitId) {
		this.unitId = unitId;
	}

	/**
	 * 取得是否是有效记录(Y:有效, N:无效)
	 */
	public String getEnabled() {
		return enabled;
	}

	/**
	 * 设置是否是有效记录(Y:有效, N:无效)
	 */
	public void setEnabled(String enabled) {
		this.enabled = enabled;
	}
}
